## o primeiro item é o executável
## a partir do segundo, todos são parâmetros

## Exemplo 1
echo oi

## Exemplo 2
echo "olá mundo"

## Exemplo 3
echo ola mundo

## No Exemplo 1, echo é o executável, e oi é o parâmetro 1.
## No Exemplo 2, echo é o executável, e "olá mundo" é o parâmetro 1. PS: Perceba que as aspas, fazem parte desse parâmetro
## No Exemplo 3, echo é o executável, e 'ola' é o parâmetro 1 e o 'mundo' o parâmetro 2. PS: Perceba que as aspas, fazem parte desse parâmetro
