## São arquivos executáveis no sistema

## No MS Windows, basta ser uma das extensões que o Windows identifica como executável, exemplo:
## bat, cmd, exe

## No Unix (BSD, Linux), nenhum arquivo é essencialmente executável, para o ser, é preciso que
## este tenha a permissão 'X' na listagem de permissões do arquivo
## Para adicionar a permissão de execução executa-se: chmod +x arquivo_alvo
## Para rmeover a permissão de execução executa-se: chmod -x arquivo_alvo
